package com.app.strings;

public class Caracteres {

	public static void main(String[] args) {
		char f = 'f';
		char u = 'u';
		char l = 'l';
		char s = 's';
		char a = 'a';
		char t = 't';
		char c = 'c';
		char k = 'k';
		//Ejercicio: escribe un programa que muestre por pantalla 
		//los caracteres individuales de una determinada palabra
		//utilizando variables de tipo char. Pág. 407
		System.out.println(f);
		System.out.println(u);
		System.out.println(l);
		System.out.println(l);
		System.out.println(s);
		System.out.println(t);
		System.out.println(a);
		System.out.println(c);
		System.out.println(k);
		//Forma con operador de asignación =
		String nombre1 = "Alejandro";
		System.out.println(nombre1);
		// otra forma de declarar un String
		String nombre2 = new String("Marcos");
		System.out.println(nombre2);
	}

}
